public class PropertyGeo {
    private long id;
    private String property_id;
    private String mls_number;
    private Float longitude;
    private Float latitude;

    public String getProperty_id() {
        return property_id;
    }

    public void setProperty_id(String property_id) {
        this.property_id = property_id;
    }

    public String getMls_number() {
        return mls_number;
    }

    public void setMls_number(String mls_number) {
        this.mls_number = mls_number;
    }

    public Float getLogitude() {
        return longitude;
    }

    public void setLogitude(Float logitude) {
        this.longitude = logitude;
    }

    public Float getLatitude() {
        return latitude;
    }

    public void setLatitude(Float latitude) {
        this.latitude = latitude;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }
}
